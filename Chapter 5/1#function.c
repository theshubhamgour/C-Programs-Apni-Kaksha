#include <stdio.h>

// Declaration or function prototype
void Helloworld();
int main()
{
    // function calling...
    HelloWorld();
    HelloWorld();
    HelloWorld();
    return (0);
}

// function definition
void HelloWorld()
{
    printf("\nHello Ji!\n");
    printf("My name is Barry Allen");
}

/*
Properties:
- Execution always starts from main
- A function gets called directly or indirectly from main
- There can be multiple functions in a program
*/